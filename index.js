let menu = document.querySelector('#menu-bars');   /*trả về phần tử đầu tiên là phần tử con của phần tử mà nó được gọi ra khớp với nhóm bộ chọn được chỉ định */
let navbar = document.querySelector('.navbar');

menu.onclick = () =>{
    menu.classList.toggle('fa-times');   /*classList.toggle('ADD') để thêm class “ADD” vào thẻ<h2>*/ 
    navbar.classList.toggle('active');
}


window.onscroll = () =>{         /*Sự kiên cuộn window.sonscroll */
    
    menu.classList.remove('fa-times');
    navbar.classList.remove('active');
}

document.querySelector('#search-icon').onclick = () =>{
    document.querySelector('#search-form').classList.toggle('active');
}

document.querySelector('#close').onclick = () =>{
    document.querySelector('#search-form').classList.remove('active');
}

